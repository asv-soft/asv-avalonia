namespace Asv.Avalonia;

public enum ContentDialogButton
{
    /// <summary>
    /// No button is specified as the default.
    /// </summary>
    None = 0,

    /// <summary>
    /// The primary button is the default.
    /// </summary>
    Primary = 1,

    /// <summary>
    /// The secondary button is the default.
    /// </summary>
    Secondary = 2,

    /// <summary>
    /// The close button is the default.
    /// </summary>
    Close = 3,
}
